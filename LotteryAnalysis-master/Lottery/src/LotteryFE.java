    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        //save all the data being submitted
        //When this button is clicked all the combinations will be generated
        String start = jTextField1.getText();
        Boolean temp = inf.getNumber().is_number(start);
        String end = jTextField2.getText();
        Boolean temp2 = inf.getNumber().is_number(end);
        String total = jTextField5.getText();
        Boolean temp4 = inf.getNumber().is_number(total);
        //check if these are all numbers and other conditions
        if (!(temp && temp2 && temp4)) {
            //clear all the jTextFields and show the user an error message
            inf.getNumber().ErrorMessage("One or more fields have invalid data types!");
            inf.setSubmitted(false);
            return;
        }

        //these are valid data types now convert them into integers
        int start_int = Integer.parseInt(start);
        int end_int = Integer.parseInt(end);
        int total_int = Integer.parseInt(total);

        if(!(jCheckBox5.isSelected() && !jTextField6.getText().isEmpty())&&!inf.getNumber().are_valid(start_int, end_int, total_int)) {
            //not valid data types, generate second error message and clear jTextFields
            inf.getNumber().ErrorMessage("Data doesn't fit into requirements!");
            inf.setSubmitted(false);
            return;
        }
        
        if((!jCheckBox2.isSelected() && jTextField4.getText().isEmpty()) && (!jCheckBox5.isSelected() && jTextField6.getText().isEmpty()) )
        {
            //not valid data types, generate second error message and clear jTextFields
            inf.getNumber().ErrorMessage("Missing a Checkbox!");
            inf.setSubmitted(false);
            return;
        }
        //set submitted to true and save info
        if(jTextField7.getText().isEmpty() == false && !inf.getNumber().is_number(jTextField7.getText()) && Integer.parseInt(jTextField7.getText()) <= 0){
            inf.getNumber().ErrorMessage("Data doesn't fit into requirements!");
            inf.setSubmitted(false);
            return;
        }
        inf.setSubmitted(true);
        inf.setStart(start_int);
        inf.setEnd(end_int);
        inf.setTotal(total_int);
        inf.getAnalysis().setFrequency(new int[end_int - start_int + 1]);
        inf.getAnalysis().setTotal(0);
        inf.setIndex(total_int);
        inf.setFrq(new Frequency[inf.getEnd() - inf.getStart() + 1]);
        inf.setFile_name(jTextField4.getText());
        if(jTextField7.getText().isEmpty() == true && jCheckBox2.isSelected()) {
            inf.getNumber().ErrorMessage("Missing number of combinations to be generated!");
            return;
        }
        inf.setNum_of_combinations(Integer.parseInt(jTextField7.getText()));
  
        return;
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
       //check if file has been selected
        inf.getAnalysis().setSuccessful(false);
        if((jCheckBox2.isSelected() == true) && (jTextField4.getText().isEmpty() == false) && inf.isSubmitted() == true) {
            inf.setFile_name(jTextField4.getText());
            inf.setPrint_to_file(true);  
            inf.getNumber().ReadFromFile(inf, inf.getFile_name());
            if(inf.getAnalysis().isSuccessful())
                inf.getNumber().DisplayResults(inf, jTextArea1);
            return;
        }
        else if(jTextField6.getText().isEmpty() == false && jCheckBox5.isSelected()) {
            String sequence = new String();
             inf.getNumber().isValidData2(jTextField6.getText(), inf);  
             if(inf.getSequence().length < inf.getTotal())
                 inf.getNumber().ErrorMessage("Sequence is smaller than quantity of numbers to be picked!");
             return;
        }
        else {
            inf.setPrint_to_file(false);
            inf.getNumber().ErrorMessage("Please input file name to read from \n and/or required information!");
            return;
        }
    }//GEN-LAST:event_jButton6ActionPerformed

