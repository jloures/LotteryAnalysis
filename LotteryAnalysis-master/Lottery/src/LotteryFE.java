    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //generate combinations
        if(inf.isSubmitted() == false) {
            inf.getNumber().ErrorMessage("Please press button Submit Data!");
            return;
        }
       
        jTextArea1.setText("");
        inf.setArray_numbers(new int[inf.getTotal()]);
        inf.getNumber().Organize(inf.getAnalysis().getFrequency(),inf);
        if(jCheckBox5.isSelected())
            inf.getNumber().generate_combinations_from_sequence(inf.getTotal(),0, inf.getSequence().length - 1, inf.getTotal(), inf, jTextArea1);
        else inf.getNumber().generate_combinations_based_on_data(inf,jTextArea1);
        inf.setCombination_number(0);
        return;
    }//GEN-LAST:event_jButton1ActionPerformed

